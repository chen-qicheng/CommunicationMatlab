function Preamble = pregen()
ShortTrainingSymbols = sqrt(13/6) * ... % 导频的频域数据
                       [0 0  1+1j 0 ...
                        0 0 -1-1j 0 ...
                        0 0  1+1j 0 ...
                        0 0 -1-1j 0 ...
                        0 0 -1-1j 0 ...
                        0 0  1+1j 0 ...
                        0 0  0    0 ...
                        0 -1-1j 0 0 ...
                        0 -1-1j 0 0 ...
                        0  1+1j 0 0 ...
                        0  1+1j 0 0 ...
                        0  1+1j 0 0 ...
                        0  1+1j 0 0].';
UsedSubcIdx = [7:32 34:59]; % 有效子载波序号
ReMappingIdx=[33:64 1:32]; % 重新按照子载波序号排序
FreqDomain = zeros(64, 1);
FreqDomain(UsedSubcIdx) = ShortTrainingSymbols;
IFFTIn = zeros(64, 1);
IFFTIn(ReMappingIdx) = FreqDomain;
ShortTrainingTime = ifft(IFFTIn);
Preamble = repmat([ShortTrainingTime(49:64); ShortTrainingTime], 100, 1);


